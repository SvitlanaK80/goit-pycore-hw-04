def get_cats_info(path):
    """
    –§—É–Ω–∫—Ü—ñ—è –∑—á–∏—Ç—É—î —Ñ–∞–π–ª —ñ–∑ –∫–æ—Ç–∞–º–∏ —Ç–∞ –ø–æ–≤–µ—Ä—Ç–∞—î —Å–ø–∏—Å–æ–∫ —Å–ª–æ–≤–Ω–∏–∫—ñ–≤.
    
    –ê—Ä–≥—É–º–µ–Ω—Ç–∏:
    - path: —à–ª—è—Ö –¥–æ —Ñ–∞–π–ª—É –∑ –¥–∞–Ω–∏–º–∏ –ø—Ä–æ –∫–æ—Ç—ñ–≤.
    
    –ü–æ–≤–µ—Ä—Ç–∞—î:
    - –°–ø–∏—Å–æ–∫ —Å–ª–æ–≤–Ω–∏–∫—ñ–≤, –¥–µ –∫–æ–∂–µ–Ω —Å–ª–æ–≤–Ω–∏–∫ –º—ñ—Å—Ç–∏—Ç—å 'id', 'name', 'age'.
    """
    cats_list = []  # –°—Ç–≤–æ—Ä—é—î–º–æ –ø–æ—Ä–æ–∂–Ω—ñ–π —Å–ø–∏—Å–æ–∫ –¥–ª—è –∑–±–µ—Ä–µ–∂–µ–Ω–Ω—è —ñ–Ω—Ñ–æ—Ä–º–∞—Ü—ñ—ó –ø—Ä–æ –∫–æ—Ç—ñ–≤

    try:
        # –í—ñ–¥–∫—Ä–∏–≤–∞—î–º–æ —Ñ–∞–π–ª –¥–ª—è —á–∏—Ç–∞–Ω–Ω—è (utf-8 –¥–ª—è –ø—ñ–¥—Ç—Ä–∏–º–∫–∏ –∫–∏—Ä–∏–ª–∏—Ü—ñ)
        with open(path, "r", encoding="utf-8") as file:
            # –ß–∏—Ç–∞—î–º–æ –≤—Å—ñ —Ä—è–¥–∫–∏ —É —Ñ–∞–π–ª—ñ
            for line in file:
                # –í–∏–¥–∞–ª—è—î–º–æ –∑–∞–π–≤—ñ –ø—Ä–æ–±—ñ–ª–∏/–ø–µ—Ä–µ–Ω–æ—Å–∏ —Ä—è–¥–∫—ñ–≤ —Ç–∞ —Ä–æ–∑–±–∏–≤–∞—î–º–æ —Ä—è–¥–æ–∫ –ø–æ –∫–æ–º—ñ
                parts = line.strip().split(",")
                
                # –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —á–∏ —î –≤—Å—ñ —Ç—Ä–∏ —á–∞—Å—Ç–∏–Ω–∏ (id, name, age)
                if len(parts) == 3:
                    cat_id, name, age = parts  # –†–æ–∑–ø–∞–∫–æ–≤—É—î–º–æ –∑–Ω–∞—á–µ–Ω–Ω—è
                    
                    # –°—Ç–≤–æ—Ä—é—î–º–æ —Å–ª–æ–≤–Ω–∏–∫ –¥–ª—è –∫–æ—Ç–∞ —Ç–∞ –¥–æ–¥–∞—î–º–æ –π–æ–≥–æ —É —Å–ø–∏—Å–æ–∫
                    cats_list.append({
                        "id": cat_id,
                        "name": name,
                        "age": int(age)  # –ö–æ–Ω–≤–µ—Ä—Ç—É—î–º–æ –≤—ñ–∫ —É —á–∏—Å–ª–æ
                    })
        
        return cats_list  # –ü–æ–≤–µ—Ä—Ç–∞—î–º–æ —Å–ø–∏—Å–æ–∫ –∫–æ—Ç—ñ–≤

    except FileNotFoundError:
        print("–ü–æ–º–∏–ª–∫–∞: –§–∞–π–ª –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ.")
        return []  # –ü–æ–≤–µ—Ä—Ç–∞—î–º–æ –ø–æ—Ä–æ–∂–Ω—ñ–π —Å–ø–∏—Å–æ–∫
    except Exception as e:
        print(f"–°—Ç–∞–ª–∞—Å—è –ø–æ–º–∏–ª–∫–∞: {e}")
        return []  # –ü–æ–≤–µ—Ä—Ç–∞—î–º–æ –ø–æ—Ä–æ–∂–Ω—ñ–π —Å–ø–∏—Å–æ–∫ —É —Ä–∞–∑—ñ —ñ–Ω—à–∏—Ö –ø–æ–º–∏–ª–æ–∫

# üîπ –í–∏–∫–ª–∏–∫ —Ñ—É–Ω–∫—Ü—ñ—ó
cats_info = get_cats_info("cats.txt")  # –í–∫–∞–∂–∏ —à–ª—è—Ö –¥–æ —Ñ–∞–π–ª—É –∑ –∫–æ—Ç–∞–º–∏
print("–Ü–Ω—Ñ–æ—Ä–º–∞—Ü—ñ—è –ø—Ä–æ –∫–æ—Ç—ñ–≤:", *cats_info, sep="\n")
